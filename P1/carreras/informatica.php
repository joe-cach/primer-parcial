<h2>Antecedentes Históricos de la FCPN</h2>
Mencionar el origen de la Facultad de Ciencias Puras y Naturales (FCPN), es referirse al inicio de la misma Universidad Mayor de San Andrés (UMSA), cuando el 30 de noviembre de 1830, se inauguró la Universidad Menor de San Andrés, con los grados de profesor, bachiller o licenciado.

El 13 de agosto del año 1831, mediante la Ley de la Asamblea General Constituyente, se elevó el rango a Universidad Mayor de San Andrés, la cual podía conceder el título de doctor. En el año 1841 y 1847, se enseñaban Matemáticas Elevadas, Historia Natural, Física, Química y los títulos otorgados fueron de: Agrimensor, Topógrafo e Ingeniero.

El 25 de mayo de 1966, fué creado el Instituto Superior de Ciencias Básicas, en remplazo del Instituto de Ciencias Exactas, que dió origen a la Facultad de Ciencias Puras y Naturales fecha en la que conmemoramos el nacimiento de nuestra Facultad.

En septiembre de 1966, fueron designados los primeros Directores de los Departamentos en Física, Abelardo Alarcón, en Química, Rolando Sahonero y en Matemáticas, Walter Alarcón. También en septiembre se inició la compra de equipos para los Departamentos de Física y de Química, que fueron los más completos y adelantados para ese tiempo, y gracias a ellos se pudo combinar la enseñanza teórica práctica.

En 1983, se procedió a la reestructuración de la Facultad de Ciencias Puras y Naturales, implementando la organización por carreras que está vigente hasta la actualidad: Física, Matemáticas, Química, Biología, Estadística e Informática.

Se crearon los institutos de investigación de cada carrera: Instituto de Investigaciones Físicas, Instituto de Investigación Matemática, Instituto de Investigaciones Químicas, Instituto de Productos Naturales, Instituto de Ecología, Instituto de Biología Molecular y Biotecnología, Instituto de Investigaciones en Informática, Instituto de Estadística Teórica y Aplicada. Además, el Instituto de Gas entre nuestra Facultad y la Facultad de Ingeniería.

<h2>Misión FCPN</h2>
La misión de la FCPN es generar conocimiento científico y tecnológico, y formar investigadores en ciencias puras y naturales altamente calificados con pensamiento crítico, iniciativa, innovadoras, emprendedoras y con responsabilidad social y ambiental; para construir una sociedad boliviana libre, justa, desarrollada, productiva, inclusiva y competitiva. (Segundo Congreso FCPN, 2016)

<h2>Visión FCPN</h2>
La visión de la FCPN es ser una facultad de excelencia reconocida internacionalmente, que lidera la transformación científica y tecnológica del país. (Segundo Congreso FCPN, 2016)
